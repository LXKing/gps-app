package cn.com.gps169.common.gis;

public class Coordinate {
    private String lat;
    private String lon;

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLat() {
        return this.lat;
    }

    public void setLon(String lon) {
        this.lon = lon;
    }

    public String getLon() {
        return this.lon;
    }
}
